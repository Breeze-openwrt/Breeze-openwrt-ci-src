#!/bin/bash

# =========================================================================================
# 🎓【初二小白课堂：源码阅读与赏析】 - clean_ubuntu.sh
# 
# 📘【剧本背景】：
# GitHub Actions 开箱免费给我们借用的一台 Ubuntu 虚拟机（你可以理解成一台刚买来的电脑）。
# 但是，微软为了照顾各种稀奇古怪的程序员，出厂就预装了 Android 开发包、.NET 环境、Haskell 语言包甚至巨型 Docker 镜像。
# 问题来了：我们只是用来编译 OpenWrt 路由器固件的！这些破烂大概占了 30 多 GB 的极其宝贵的硬盘空间，
# 如果不删，等会我们下载 OpenWrt 的上万个源码文件时，硬盘一定会“Boom”（爆缸）！
#
# 🕵️‍♂️【作者意图】：
# 这里直接动用原生系统自带的极其暴力的超级指令 `rm -rf`，一秒钟瞬间抹除系统出厂自带的安卓等无用工厂！
#
# 💡【性能狂魔设计模式】：
# 避免使用第三方扫地大妈 jlumbroso 插件（它喜欢磨洋工耗时达 3~5 分钟）。
# 我们采用“精准定点爆破”模式，只用仅仅两行核心指令，几秒钟白嫖 30GB 空间！
# =========================================================================================

# -----------------------------------------------------
# 【行级注释与语法解释开始】
# -----------------------------------------------------

# `echo` 就是让电脑屏幕向我们“喊话”输出文字信息，用来做记录。
echo "📊 强力拆迁前系统总可用空间："

# `df` = Disk Free (查看磁盘剩余空间)。
# `-h` = Human-readable (也就是给你显示成多少 GB、MB，而不是一堆看不懂的数字)。
# `-T` = Type (告诉你这是什么文件系统格式，比如 ext4)。
df -hT

echo "🧨 警告：正在引爆云端的旧有原厂巨兽建筑..."

# `sudo` = SuperUser DO，向系统借用最高管理员“玉皇大帝”权限！
# `rm` = ReMove (删除)。
# `-r` = Recursive（递归，也就是俗称的“连根拔起”，目录里面的文件夹也一窝端）。
# `-f` = Force（强制，就算找不到也不许它报错嚷嚷，干死为止）。
# 后面跟的就是占位置最狠的 Android、.NET 和各种代码审计工具目录。
sudo rm -rf /usr/share/dotnet /usr/local/lib/android /opt/ghc /opt/hostedtoolcache/CodeQL

# 第二波定点爆破，炸掉 Boost 库、苹果开发语言包 Swift、Java 虚拟机、迷你装的 Conda 训练环境以及占地的 Chromium 浏览器缓存。
sudo rm -rf /usr/local/share/boost /usr/share/swift /usr/lib/jvm /usr/share/miniconda /usr/local/share/chromium

# `docker image prune` = 清理无用的 Docker 镜像（像垃圾车倒垃圾）。
# `--all` = 清理所有没有被使用的历史镜像残留。
# `--force` = 强制，不要弹窗问我“你确定吗？”。
sudo docker image prune --all --force

# 报告战果！
echo "🎉 新世界建筑彻底清除完毕，已达到物理理论上的清理极值！耗时仅几秒！"
echo "📊 强力拆迁后系统总可用空间对比（见证白嫖的奇迹）："

# 再查一次看看战果，这个时候用肉眼看 GB 数字会突然大很多，极其舒爽！
df -hT

# -----------------------------------------------------
# 【本模块逻辑执行完毕宣告结束】
# -----------------------------------------------------
